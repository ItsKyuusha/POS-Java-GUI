    // hitung jual
    private void hitung_jual() {
        double xtot, xhrg;
        int xjml;
        xhrg = Double.parseDouble(hrg_jual.getText());
        xjml = Integer.parseInt(jml_jual.getText());
        xtot = xhrg * xjml;
        String xtotal = Double.toString(xtot);
        totjual.setText(xtotal);
        total = total + xtot;
        totjual.setText(Double.toString(total));
    }
    
    private void hitungTotalTransaksi() {
        double total = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            total += Double.parseDouble(tableModel.getValueAt(i, 4).toString()); // kolom total
        }
        totjual.setText(Double.toString(total));
        hitungTotal();
    }

    private void hitungTotal() {
    double total = 0;
    for (int i = 0; i < tableModel.getRowCount(); i++) {
        double harga = Double.parseDouble(tableModel.getValueAt(i, 2).toString());
        int jumlah = Integer.parseInt(tableModel.getValueAt(i, 3).toString());
        total += (harga * jumlah);
    }
    totjualdetail.setText(String.valueOf(total));
}
    
    private void baca_konsumen() {
        try {
            stm = Con.createStatement();
            ResultSet rs = stm.executeQuery("SELECT kd_kons, nm_kons FROM konsumen");
            kd_kons.removeAllItems();
            while(rs.next()) {
                kd_kons.addItem(rs.getString("kd_kons"));
            }
            rs.close();
        } catch(SQLException e) {
            System.out.println("Error baca_konsumen : " + e.getMessage());
        }
    }

