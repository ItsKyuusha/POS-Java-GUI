    private void detail_barang(String xkode) {
        try {
            Statement stmt = Con.createStatement();
            String sql = "SELECT kd_brg, nm_brg, harga_jual FROM barang WHERE kd_brg='" + xkode + "'";
            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {
                kd_brg.setSelectedItem(rs.getString("kd_brg")); // set combobox / textfield
                nm_brg.setText(rs.getString("nm_brg"));
                hrg_jual.setText(String.valueOf(rs.getDouble("harga_jual")));
            } else {
                nm_brg.setText("");
                hrg_jual.setText("");
            }

            rs.close();
            stmt.close();
        } catch (SQLException e) {
            System.out.println("Error detail_barang : " + e.getMessage());
        }
    }

