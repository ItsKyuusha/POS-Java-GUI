    // nomor jual otomatis
    private void nomor_jual() {
        try {
            stm = Con.createStatement();
            ResultSet rs = stm.executeQuery("SELECT no_jual FROM jual");
            int brs = 0;
            while (rs.next()) {
                brs = rs.getRow();
            }
            if (brs == 0) {
                no_jual.setText("1");
            } else {
                int nom = brs + 1;
                no_jual.setText(Integer.toString(nom));
            }
            rs.close();
        } catch (SQLException e) {
            System.out.println("Error nomor_jual : " + e.getMessage());
        }
    }

    // simpan transaksi
    private void simpan_transaksi() {
    try {
        String xnojual = no_jual.getText();
        format_tanggal();
        String xkode = kd_kons.getSelectedItem().toString();

        // simpan ke tabel jual
        String msql = "INSERT INTO jual VALUES('" + xnojual + "','" + xkode + "','" + tanggal + "')";
        stm.executeUpdate(msql);

        // simpan ke tabel detail (djual)
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            String xkd = (String) tableModel.getValueAt(i, 0); // kd_brg
            double xhrg = Double.parseDouble(tableModel.getValueAt(i, 2).toString());
            int xjml = Integer.parseInt(tableModel.getValueAt(i, 3).toString());

            // simpan detail jual
            String zsql = "INSERT INTO djual VALUES('" + xnojual + "','" + xkd + "'," + xhrg + "," + xjml + ")";
            stm.executeUpdate(zsql);

            // update stok barang
            String usql = "UPDATE barang SET stok = stok - " + xjml + " WHERE kd_brg = '" + xkd + "'";
            stm.executeUpdate(usql);
        }

        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error simpan_transaksi : " + e.getMessage());
    }
}


    // format tanggal
    private void format_tanggal() {
        String DATE_FORMAT = "yyyy-MM-dd";
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat(DATE_FORMAT);
        Calendar c1 = Calendar.getInstance();
        int year = c1.get(Calendar.YEAR);
        int month = c1.get(Calendar.MONTH) + 1;
        int day = c1.get(Calendar.DAY_OF_MONTH);
        tanggal = year + "-" + month + "-" + day;
    }
    
    // Inner class untuk printing
private class PrintingTask extends SwingWorker<Object, Object> { 
    private final MessageFormat headerFormat; 
    private final MessageFormat footerFormat; 
    private final boolean interactive; 
    private volatile boolean complete = false; 
    private volatile String message; 

    public PrintingTask(MessageFormat header, MessageFormat footer, boolean interactive) { 
        this.headerFormat = header; 
        this.footerFormat = footer; 
        this.interactive = interactive; 
    } 

    @Override 
    protected Object doInBackground() { 
        try { 
            complete = print.print(headerFormat, footerFormat, true, null, null, interactive); 
            message = "Printing " + (complete ? "complete" : "canceled"); 
        } catch (PrinterException ex) { 
            message = "Sorry, a printer error occurred"; 
        } catch (SecurityException ex) { 
            message = "Sorry, cannot access the printer due to security reasons"; 
        } 
        return null; 
    } 

    @Override 
    protected void done() { 
        JOptionPane.showMessageDialog(FormTransaksiPenjualan.this, message); 
    } 
}
    
