    private void cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakActionPerformed
        StringBuilder ctk = new StringBuilder();

    // ===== Header Toko =====
    ctk.append("WARWA - Warung Jawa\n");
    ctk.append("Jl. Jawir No.123, Kota Semarang\n");
    ctk.append("Telp: 0812-XXXX-XXXX\n");
    ctk.append("===========================================\n");
    ctk.append(String.format("Nota Penjualan No: %s%n", no_jual.getText()));
    ctk.append(String.format("Tanggal         : %s%n", tanggal));
    ctk.append("===========================================\n");

    // ===== Daftar Barang =====
    ctk.append(String.format("%-10s %-20s %10s %5s %10s%n", "Kode", "Nama Barang", "Harga", "Jml", "Total"));
    ctk.append("-----------------------------------------------------------------------\n");

    double totalNota = 0;
    for (int i = 0; i < tblJual.getRowCount(); i++) {
        String xkd = (String) tblJual.getValueAt(i, 0);
        String xnama = (String) tblJual.getValueAt(i, 1);
        double xhrg = Double.parseDouble(tblJual.getValueAt(i, 2).toString());
        int xjml = Integer.parseInt(tblJual.getValueAt(i, 3).toString());
        double xtot = Double.parseDouble(tblJual.getValueAt(i, 4).toString());
        totalNota += xtot;

        ctk.append(String.format("%-10s %-20s %10.2f %5d %10.2f%n", xkd, xnama, xhrg, xjml, xtot));
    }

    ctk.append("--------------------------------------------------------------\n");

    // ===== Total, Bayar, Kembalian =====
    ctk.append(String.format("%-35s %10.2f%n", "TOTAL :", totalNota));

    try {
        String bayarStr = bayardetail.getText().trim();
        if (!bayarStr.isEmpty()) {
            double bayarNom = Double.parseDouble(bayarStr);
            double kembalianNom = bayarNom - totalNota;
            ctk.append(String.format("%-35s %10.2f%n", "BAYAR :", bayarNom));
            ctk.append(String.format("%-35s %10.2f%n", "KEMBALIAN :", kembalianNom));
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Input bayar harus angka!");
    }

    // ===== Set ke TextArea =====
    print.setText(ctk.toString());

    // ===== Cetak =====
    MessageFormat header = new MessageFormat("");
    MessageFormat footer = new MessageFormat("");
    boolean interactive = true;
    PrintingTask task = new PrintingTask(header, footer, interactive);
    task.execute();
    }//GEN-LAST:event_cetakActionPerformed

